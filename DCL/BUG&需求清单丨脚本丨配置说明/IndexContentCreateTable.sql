		EXECUTE IMMEDIATE 'COMMENT ON COLUMN T_INDEX_CONTENT.REGION_CODE  IS ''所属区域''';
		EXECUTE IMMEDIATE 'COMMENT ON COLUMN T_INDEX_CONTENT.UUID  IS ''UUID''';
		EXECUTE IMMEDIATE 'COMMENT ON COLUMN T_INDEX_CONTENT.TYPE  IS ''展示类型''';
		EXECUTE IMMEDIATE 'COMMENT ON COLUMN T_INDEX_CONTENT.URL  IS ''链接地址''';
