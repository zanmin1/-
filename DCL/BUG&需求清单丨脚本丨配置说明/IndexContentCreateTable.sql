PROMPT SEQ_INDEX_CONTENT  
DECLARE
	FLAG_NUM NUMBER;
BEGIN
	SELECT COUNT(*) INTO FLAG_NUM
	FROM USER_SEQUENCES ASQ
	WHERE ASQ.SEQUENCE_NAME = 'SEQ_INDEX_CONTENT';
	IF FLAG_NUM = 0 THEN 
		EXECUTE IMMEDIATE 'CREATE SEQUENCE SEQ_INDEX_CONTENT MINVALUE 1 MAXVALUE 999999999999 START WITH 1 INCREMENT BY 1 CACHE 20'; 
	END IF;
END;
/
COMMIT;



